package tpfinalMDC;

public class Bool {

    private boolean value;

    public Bool() {
        this.value = false;
    }

    public boolean is() {
        return this.value;
    }

    public void setTrue() {
        this.value = true;
    }
    public void set(boolean b) {
    	this.value=b;
    }
    public void setFalse() {
        this.value = false;
    }
}
